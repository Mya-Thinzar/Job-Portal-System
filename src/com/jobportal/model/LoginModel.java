package com.jobportal.model;

public class LoginModel {
	private String frmLoginId;
	private String frmUserName;
	private String frmEmail;
	private String frmPassword;
	private String frmNewPassword;
	private String frmRePassword;
	
	public String getFrmLoginId() {
		return frmLoginId;
	}
	public void setFrmLoginId(String frmLoginId) {
		this.frmLoginId = frmLoginId;
	}
	public String getFrmUserName() {
		return frmUserName;
	}
	public void setFrmUserName(String frmUserName) {
		this.frmUserName = frmUserName;
	}
	public String getFrmEmail() {
		return frmEmail;
	}
	public void setFrmEmail(String frmEmail) {
		this.frmEmail = frmEmail;
	}
	public String getFrmPassword() {
		return frmPassword;
	}
	public void setFrmPassword(String frmPassword) {
		this.frmPassword = frmPassword;
	}
	public String getFrmNewPassword() {
		return frmNewPassword;
	}
	public void setFrmNewPassword(String frmNewPassword) {
		this.frmNewPassword = frmNewPassword;
	}
	public String getFrmRePassword() {
		return frmRePassword;
	}
	public void setFrmRePassword(String frmRePassword) {
		this.frmRePassword = frmRePassword;
	}
	
}
